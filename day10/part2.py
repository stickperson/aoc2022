def compute(s: str) -> list[str]:
    x_register = 1
    crt_position = 0

    instructions = s.splitlines()
    matrix: list[str] = []
    for _ in range(6):
        matrix.append("." * 40)

    for inst in instructions:
        val = 0
        if inst.startswith('noop'):
            cycles_to_add = 1
        else:
            cycles_to_add = 2
            val = int(inst.split()[1])

        for _ in range(cycles_to_add):
            # There;s some off by 1 error here
            row = (crt_position) // 40
            col = (crt_position) % 40
            mark = "."
            if abs((x_register % 40) - col) <= 1:
                mark = "#"
            x = list(matrix[row])
            x[col] = mark
            matrix[row] = "".join(x)
            crt_position += 1
        x_register += val
        x_register = x_register % 40

    output = []
    for m in matrix:
        output.append("".join(m))
    print("\n".join(matrix))
    return matrix


