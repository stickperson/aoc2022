def compute(s: str) -> int:
    signal_strengths: list[int] = []
    x = 1
    cycle = 1
    instructions = s.splitlines()
    for inst in instructions:
        value = 0
        if inst.startswith('noop'):
            cycles_to_add = 1
        else:
            cycles_to_add = 2
            value = int(inst.split()[1])
        for idx in range(cycles_to_add):
            if cycle % 40 == 20:
                signal_strengths.append(x * cycle)
            cycle += 1
        x += value
    return sum(signal_strengths)


